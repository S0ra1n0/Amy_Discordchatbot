def help_list():
    s = '"!on" to turn on\n'
    s += '"!off" to turn off\n'
    s += '"!again" to redo the prev command\n'
    s += '"!1dice" to roll 1 dice\n'
    s += '"!2dices" to roll 2 dices\n'
    s += '"!multidiceface" to roll multi dices with multi faces\n'
    s += '"!rng" to generate a random number\n'
    s += '"!time" to display the time\n'
    s += '"!wikipedia" to search about something\n'
    s += '"!cointoss" to toss a coin\n'
    s += '"!translate" to translate a text\n'
    s += '"!currency_exchange" to convert currency\n'

    return s